CREATE OR ALTER PROCEDURE sc_detalleforward_crear
    @seq INT,
    @fechapago DATE,
    @idforward INT,
    @tipopago VARCHAR(200),
    @formapago VARCHAR(200),
    @trm NUMERIC(18,2),
    @valor NUMERIC(18,2),
    @usuariocrea VARCHAR(50),
    @seqpago INT
AS
    INSERT INTO detalleforward VALUES
    (
        @seq,
        YEAR(@fechapago),
        MONTH(@fechapago),
        @fechapago,
        @idforward,
        @tipopago,
        @formapago,
        @trm,
        @valor,
        'ACTIVO',
        @usuariocrea,
        GETDATE(),
        @usuariocrea,
        GETDATE(),
        @seqpago
    )

	UPDATE creditoforward
	SET
		saldoasignacion = saldoasignacion - @valor,
		fechamod = GETDATE(),
		usuariomod = @usuariocrea
	WHERE 
		seq = @seqpago
GO


CREATE OR ALTER PROCEDURE [dbo].[sc_actualizar_saldos]
	@idforward int = null,
	@idcredito int = null
AS
	UPDATE credito SET 
		saldo = capital - (SELECT ISNULL(SUM(valor),0) FROM detallepago WHERE credito.id = detallepago.idcredito AND detallepago.tipopago = 'Capital'),
		saldoasignacion = capital - (SELECT ISNULL(SUM(valorasignado),0) FROM creditoforward WHERE credito.id = creditoforward.idcredito)
	WHERE
		id = ISNULL(@idcredito, id)
	UPDATE forward SET
		saldo = valorusd - (SELECT ISNULL(SUM(valor),0) FROM detalleforward WHERE forward.id = detalleforward.idforward AND detalleforward.formapago = 'FORWARD' ),
		saldoasignacion = valorusd - (SELECT ISNULL(SUM(valorasignado),0) FROM creditoforward WHERE forward.id = creditoforward.idforward)
    WHERE
        estado <> 'CERRADO'
		AND id = ISNULL(@idforward, id)

	UPDATE credito SET 
		estado = 'PAGO'
	WHERE
		saldo = 0
		AND estado = 'ACTIVO'
		AND id = ISNULL(@idcredito, id)
	
	UPDATE credito SET 
		estado = 'ACTIVO'
	WHERE
		saldo != 0
		AND estado = 'PAGO'
		AND id = ISNULL(@idcredito, id)
	
	UPDATE creditoforward SET
		saldoasignacion = valorasignado - (SELECT ISNULL(SUM(valor),0) FROM detalleforward WHERE creditoforward.idforward = detalleforward.idforward AND detalleforward.formapago = 'FORWARD' AND creditoforward.seq = detalleforward.seqpago)
	WHERE
		idcredito = ISNULL(@idcredito, idcredito)
		and idforward = ISNULL(@idforward, idforward)
GO


